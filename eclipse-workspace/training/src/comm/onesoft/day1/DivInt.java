package comm.onesoft.day1;

public class DivInt {
	public static void main(String[] args) {
		int num1 = 4466;
		int num2 = 6566;
		int total1 = num1 / num2;

		long num3 = 66462649l;
		long num4 = 656666l;
		long total2 = num3 / num4;

		float num5 = 65.2f;
		float num6 = 2.25f;
		float total3 = num5 / num6;

		double num7 = 6685.566d;
		double num8 = 656.365d;
		double total4 = num7 / num8;

		System.out.println("the value of int are :" + total1);
		System.out.println("the value of long are :" + total2);
		System.out.println("the value o  float are :" + total3);
		System.out.println("the value of double are :" + total4);

	}

}
